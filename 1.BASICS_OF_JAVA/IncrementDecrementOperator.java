public class IncrementDecrementOperator {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x);
        x++;//means x=x+1
        System.out.println(x);
        x--;// x=x-1
        System.out.println(x);
        //this is a single line comment
        /*
         this is use for multiple comment
        */


    }
}