 public class Logical_operator {
    public static void main(String[] args) {
    int p=14,q=10,r=5 ;
        // && operator
        System.out.println((p>q)&&(p>r));
        System.out.println((p<q)&&(p<q));

        // || operator
        System.out.println((p>q)||(p>r));
        System.out.println((p<q)||(p<q));

        //  ! operator
        System.out.println();

    }
}
